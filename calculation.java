class calculation extends methods{
    public static int add(String value1, String value2){
        int val1 = Integer.parseInt(value1);
        int val2 = Integer.parseInt(value2);
        return val1 + val2;
    }
    public static int minus(String value1, String value2){
        int val1 = Integer.parseInt(value1);
        int val2 = Integer.parseInt(value2);
        return val1 - val2;
    }
    public static int multiply(String value1, String value2){
        int val1 = Integer.parseInt(value1);
        int val2 = Integer.parseInt(value2);
        return val1 * val2;
    }
    public static double divide(String value1, String value2){
        double val1 = Integer.parseInt(value1);
        double val2 = Integer.parseInt(value2);
        return val1 / val2;
    }

}

